def naked_twins(values):
    pot_twins=[box for box in boxes if len(values[box])==2]
    for pot_twin in pot_twins:
        for unit in units[pot_twin]:
            for box in unit:
                if values[box]==values[pot_twin] and box!=pot_twin:
                    for i in unit:
                        if i!=pot_twin and i!=box:
                            values[i]=values[i].replace(values[pot_twin][0],'')
                            values[i]=values[i].replace(values[pot_twin][1],'')
    return values

